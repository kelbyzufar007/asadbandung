        <script>
        (function() {
            const el = document.getElementById('bizCounter');
            if (!el) return;

            const target = parseInt(el.getAttribute('data-target') || '1000', 10);
            const duration = parseInt(el.getAttribute('data-duration') || '1400', 10);
            let started = false;

            // Easing halus
            const easeOutCubic = t => 1 - Math.pow(1 - t, 3);

            // Formatter ribuan Indonesia, mis. 1000 -> 1.000
            const fmt = n => new Intl.NumberFormat('id-ID').format(n);

            function animate() {
                const start = performance.now();

                function frame(now) {
                    const p = Math.min((now - start) / duration, 1);
                    const val = Math.round(target * easeOutCubic(p));
                    el.textContent = fmt(val);
                    if (p < 1) requestAnimationFrame(frame);
                }
                requestAnimationFrame(frame);
            }

            // Mulai saat terlihat di viewport
            const io = new IntersectionObserver(([entry]) => {
                if (entry.isIntersecting && !started) {
                    started = true;
                    animate();
                    io.unobserve(el);
                }
            }, {
                threshold: 0.3
            });

            io.observe(el);
        })();
        </script>

